package com.Q4MapInDescendingOrder;

public class Cities {

	String a;
	String b;
	String c;

	
	public Cities(String a, String b, String c) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;

	}

	@Override
	public String toString() {
		return " Cities: [" + a + ", " + b + ", " + c + "]";
	}

	
	
}
